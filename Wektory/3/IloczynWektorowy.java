public class IloczynWektorowy {

    public int Iloczyn1(int b,int f,int c,int e) {
        int A = b*f;
        int B = c*e;
        return B-A;
    }
    public int Iloczyn2(int c,int d,int a, int f) {
        int A=c*d;
        int B = a*f;
        return B-A;
    }
    public int Iloczyn3(int a,int e,int b,int d) {
        int A = a*e;
        int B = b*d;
        return B-A;
    }
}
